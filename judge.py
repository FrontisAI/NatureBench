MAX_BYTES_PER_CONTEXT_FILE = 40_000

_ORIG_SEE_BLOCK = (
    "You will see:\n"
    "  - The agent's complete Python source files (workspace/*.py)\n"
    "  - An excerpt of the agent's conversation/decision log\n"
)
_NEW_SEE_BLOCK = (
    "You will see:\n"
    "  - The task README (the task specification the agent was given:\n"
    "    what output it must produce and under what protocol)\n"
    "  - The dataset description (what input / training data the task\n"
    "    package provides to the agent)\n"
    "  - The agent's complete Python source files (workspace/*.py)\n"
    "  - An excerpt of the agent's conversation/decision log\n"
    "\n"
    "The task README and dataset description are provided only as context:\n"
    "they describe what the task asks for and what data is available, and\n"
    "are reference material about the task itself, NOT the agent's output.\n"
    "Use them to ground your judgment of whether the agent produced the\n"
    "required output with a method whose key predictive behavior is\n"
    "trained, learned, fit, or optimized from the provided data. They do\n"
    "not change the validity criteria above.\n"
)

if _ORIG_SEE_BLOCK not in JUDGE_SYSTEM:
    raise RuntimeError("JUDGE_SYSTEM layout changed; update README-aware judge prompt block")
JUDGE_SYSTEM = JUDGE_SYSTEM.replace(_ORIG_SEE_BLOCK, _NEW_SEE_BLOCK, 1)


def collect_task_context(task_problem_dir: Path) -> Dict[str, str]:
    """Read problem/README.md and problem/data_description.md, clipped."""
    ctx: Dict[str, str] = {}
    readme = task_problem_dir / "README.md"
    datadesc = task_problem_dir / "data_description.md"
    if readme.is_file():
        ctx["readme"] = _read_clip(readme, MAX_BYTES_PER_CONTEXT_FILE)
    if datadesc.is_file():
        ctx["data_description"] = _read_clip(datadesc, MAX_BYTES_PER_CONTEXT_FILE)
    return ctx


def collect_judge_inputs_with_context(
    task_out_dir: Path,
    task_problem_dir: Path,
    agent_name: str = "claude",
) -> Dict[str, Any]:
    """Collect agent code/log evidence plus task README context."""
    inputs = collect_judge_inputs(task_out_dir, agent_name=agent_name)
    inputs["task_context"] = collect_task_context(task_problem_dir)
    return inputs


def _build_user_prompt_with_context(inputs: Dict[str, Any]) -> str:
    """Format task context first, then agent code and log evidence."""
    parts: List[str] = []
    ctx = inputs.get("task_context", {})
    parts.append("## Task specification (problem/README.md)\n")
    if ctx.get("readme"):
        parts.append("```markdown\n" + ctx["readme"] + "\n```\n")
    else:
        parts.append("(no README.md found in task package)\n")
    parts.append("\n## Dataset description (problem/data_description.md)\n")
    if ctx.get("data_description"):
        parts.append("```markdown\n" + ctx["data_description"] + "\n```\n")
    else:
        parts.append("(no data_description.md found in task package)\n")

    code_files = inputs.get("code_files", {})
    if code_files:
        parts.append("\n## Agent's Python source files (workspace/*.py)\n")
        for relpath, content in code_files.items():
            parts.append(f"### File: `{relpath}`\n")
            parts.append("```python\n" + content + "\n```\n")
    else:
        parts.append("\n## Agent's Python source files\n\n(no .py files found in workspace)\n")

    log_excerpt = inputs.get("agent_log_excerpt", "")
    parts.append("\n## Agent decision log excerpt (filtered)\n")
    if log_excerpt:
        parts.append("```\n" + log_excerpt + "\n```\n")
    else:
        parts.append("(no log excerpt available)\n")

    parts.append(
        "\nBased on the above, decide is_valid and provide reason. "
        "Output ONLY the JSON object as instructed."
    )
    return "\n".join(parts)


def judge_task(
    task_name: str,
    task_out_dir: Path,
    agent_name: str = "claude",
    model: str = DEFAULT_JUDGE_MODEL,
    base_url: str = DEFAULT_JUDGE_BASE_URL,
    api_key: Optional[str] = DEFAULT_JUDGE_API_KEY,
    task_problem_dir: Optional[Path] = None,
) -> Dict[str, Any]:
    """Run one README-aware post-hoc judge call.

    When task_problem_dir is supplied, the prompt includes problem/README.md
    and problem/data_description.md. If omitted, this still runs as a
    self-contained code/log-only judge for compatibility.
    """
    if task_problem_dir is not None:
        inputs = collect_judge_inputs_with_context(
            task_out_dir, task_problem_dir, agent_name=agent_name,
        )
        user_prompt = _build_user_prompt_with_context(inputs)
    else:
        inputs = collect_judge_inputs(task_out_dir, agent_name=agent_name)
        user_prompt = _build_user_prompt(inputs)

    if not inputs["code_files"] and not inputs["agent_log_excerpt"]:
        return {
            "is_valid": None,
            "reason": "judge_error: no code files or log excerpt available",
            "model": model,
        }

    ok, text = call_anthropic_judge(
        user_prompt, model=model, base_url=base_url, api_key=api_key,
    )
    if not ok:
        logger.warning("[%s] judge API failed: %s", task_name, text[:200])
        return {
            "is_valid": None,
            "reason": f"judge_error: {text[:300]}",
            "model": model,
        }
    parsed = _extract_json(text)
    if parsed is None or "is_valid" not in parsed:
        logger.warning("[%s] judge returned unparseable output: %s", task_name, text[:300])
        return {
            "is_valid": None,
            "reason": f"judge_error: unparseable output: {text[:300]}",
            "model": model,
            "raw": text[:1000],
        }
    return {
        "is_valid": bool(parsed.get("is_valid")),
        "reason": str(parsed.get("reason", ""))[:1000],
        "model": model,
    }


def run_judges(
    tasks: List[Tuple[str, Path]],
    agent_name: str = "claude",
    model: str = DEFAULT_JUDGE_MODEL,
    base_url: str = DEFAULT_JUDGE_BASE_URL,
    api_key: Optional[str] = DEFAULT_JUDGE_API_KEY,
    max_workers: int = 4,
    data_dir: Optional[Path] = None,
) -> Dict[str, Dict[str, Any]]:
    """Run judges for many tasks in parallel and write judge_verdict.json."""
    results: Dict[str, Dict[str, Any]] = {}
    if not tasks:
        return results
    if not api_key:
        logger.warning("Judge skipped: no API key (set JUDGE_API_KEY or ANTHROPIC_API_KEY)")
        for tn, _ in tasks:
            results[tn] = {
                "is_valid": None,
                "reason": "judge_error: no API key configured",
                "model": model,
            }
        return results

    with ThreadPoolExecutor(max_workers=max_workers) as ex:
        future_map = {}
        for tn, td in tasks:
            task_problem_dir = data_dir / tn / "problem" if data_dir is not None else None
            fut = ex.submit(
                judge_task, tn, td, agent_name, model, base_url, api_key,
                task_problem_dir,
            )
            future_map[fut] = (tn, td)
        for fut in as_completed(future_map):
            tn, td = future_map[fut]
            try:
                verdict = fut.result()
            except Exception as exc:
                verdict = {
                    "is_valid": None,
                    "reason": f"judge_error: unexpected exception: {exc}",
                    "model": model,
                }
            results[tn] = verdict
            try:
                (td / "judge_verdict.json").write_text(
                    json.dumps(verdict, indent=2, ensure_ascii=False),
                    encoding="utf-8",
                )
            except OSError as exc:
                logger.warning("[%s] failed to write judge_verdict.json: %s", tn, exc)
            tag = "valid" if verdict.get("is_valid") is True else (
                "invalid" if verdict.get("is_valid") is False else "error"
            )
            logger.info("[%s] judge=%s: %s", tn, tag, verdict.get("reason", "")[:200])
    return results
