"""judge.py — post-hoc LLM judge for scientific validity.

After all tasks finish, this module reviews each agent's full code +
conversation log and returns {is_valid, reason}. Invalid submissions have
their score discarded (effective_improvement = null).

Design references:
  - MLE-Bench's rule_violation_detector (post-hoc, gpt-4o-mini)
  - judge.png (binary verdict + free-form reason, with concrete cheat patterns)

The judge runs entirely host-side after the agent container has stopped.
There is no agent-facing endpoint.
"""
