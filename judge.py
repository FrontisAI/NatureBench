def _read_clip(path: Path, max_bytes: int) -> str:
    """Read text file, clip to max_bytes (byte-aware, utf-8 safe)."""
    try:
        data = path.read_bytes()
    except OSError as e:
        return f"[failed to read {path}: {e}]"
    if len(data) <= max_bytes:
        return data.decode("utf-8", errors="replace")
    head_n = max_bytes // 2
    tail_n = max_bytes - head_n
    head = data[:head_n].decode("utf-8", errors="replace")
    tail = data[-tail_n:].decode("utf-8", errors="replace")
    return head + f"\n\n... [{len(data) - max_bytes} bytes elided] ...\n\n" + tail


def collect_code_files(workspace_dir: Path) -> Dict[str, str]:
    """Collect .py files under workspace, shallowest first.

    Priority ordering:
      1. Depth (files at workspace root before files in subdirs)
      2. Files named run.py / solution.py / main.py / train.py preferred at each depth
      3. Alphabetical by path

    Rationale: the agent's own solution code typically lives at workspace
    root; subdirectories are often cloned third-party repos. The 30-file
    cap must not silently drop the agent's own code in favor of library code.
    """
    files: Dict[str, str] = {}
    if not workspace_dir.exists():
        return files
    skip_dirs = {"__pycache__", ".git", "node_modules", ".venv", "venv", "site-packages"}
    entry_names = {"run.py", "solution.py", "main.py", "train.py"}
    candidates = []
    for py in workspace_dir.rglob("*.py"):
        if any(part in skip_dirs for part in py.parts):
            continue
        try:
            relpath = py.relative_to(workspace_dir)
        except ValueError:
            continue
        depth = len(relpath.parts) - 1
        is_entry = relpath.name in entry_names
        # Sort key: (depth, not_entry, path_str). Entry files rank above non-entry at same depth.
        candidates.append((depth, not is_entry, str(relpath), py))
    candidates.sort()
    for _depth, _not_entry, relpath_str, py in candidates:
        files[relpath_str] = _read_clip(py, MAX_BYTES_PER_PY_FILE)
        if len(files) >= MAX_PY_FILES:
            break
    return files


def _extract_snippet_claude(obj):
    """Claude stream / session jsonl — message dict with content blocks.

    Tool-use and tool-result blocks are tagged with [tool_use ...] /
    [tool_result] markers so they are symmetric with the codex/gemini
    extractors and can be retained structurally.
    """
    msg = obj.get("message")
    if not isinstance(msg, dict):
        return None
    content = msg.get("content")
    if isinstance(content, str):
        return content
    if isinstance(content, list):
        parts = []
        for c in content:
            if not isinstance(c, dict):
                continue
            ctype = c.get("type")
            if ctype == "tool_use" or ("input" in c and "name" in c):
                name = c.get("name", "")
                parts.append(
                    "[tool_use " + str(name) + "] "
                    + json.dumps(c.get("input", {}), ensure_ascii=False)[:800]
                )
            elif ctype == "tool_result" or "content" in c:
                cc = c.get("content")
                if isinstance(cc, str):
                    parts.append("[tool_result] " + cc[:800])
                elif isinstance(cc, list):
                    txt = " ".join(
                        str(x.get("text", "")) for x in cc if isinstance(x, dict)
                    )
                    parts.append("[tool_result] " + txt[:800])
            elif "text" in c:
                parts.append(str(c.get("text", "")))
            elif "input" in c:
                parts.append(
                    "[tool_use] "
                    + json.dumps(c.get("input", {}), ensure_ascii=False)[:800]
                )
        return "\n".join(p for p in parts if p)
    return None


def _extract_snippet_gemini(obj):
    """Gemini --output-format stream-json events."""
    typ = obj.get("type")
    if typ == "message":
        c = obj.get("content")
        if isinstance(c, str):
            return c
    elif typ == "tool_use":
        name = obj.get("tool_name", "")
        params = obj.get("parameters") or {}
        return "[tool_use " + str(name) + "] " + json.dumps(params, ensure_ascii=False)[:800]
    elif typ == "tool_result":
        out = obj.get("output")
        if isinstance(out, str):
            return "[tool_result] " + out[:800]
    elif typ == "result":
        return "[result status=" + str(obj.get("status", "")) + "]"
    return None


def _extract_snippet_codex(obj):
    """Codex exec --json stream events (item.completed)."""
    typ = obj.get("type")
    if typ != "item.completed":
        return None
    item = obj.get("item") or {}
    itype = item.get("type")
    if itype == "agent_message":
        t = item.get("text")
        if isinstance(t, str):
            return t
    elif itype == "command_execution":
        cmd = item.get("command", "")
        out = item.get("aggregated_output") or item.get("output") or ""
        ec = item.get("exit_code", "")
        snip = "[cmd] " + str(cmd)[:300]
        if out:
            snip += " | [out exit=" + str(ec) + "] " + str(out)[:400]
        return snip
    elif itype == "file_change":
        path = item.get("path", "")
        kind = item.get("kind", "")
        return "[file_change kind=" + str(kind) + " path=" + str(path) + "]"
    return None


def _extract_snippet_codex_rollout(obj):
    """Codex internal session rollout (.codex_state/sessions/.../rollout-*.jsonl).

    Format: {"type": session_meta|response_item|event_msg|turn_context,
             "payload": {...}}. This file is richer than the exec stream —
    it carries the full function_call arguments and tool outputs.
    """
    typ = obj.get("type")
    p = obj.get("payload")
    if not isinstance(p, dict):
        return None
    ptype = p.get("type")
    if typ == "response_item":
        if ptype == "message":
            role = p.get("role", "")
            if role in ("developer", "system"):
                return None  # injected instructions — not agent behavior
            content = p.get("content") or []
            texts = []
            if isinstance(content, list):
                for c in content:
                    if isinstance(c, dict) and "text" in c:
                        texts.append(str(c.get("text", "")))
            elif isinstance(content, str):
                texts.append(content)
            if texts:
                return "[message role=" + str(role) + "] " + "\n".join(texts)
        elif ptype == "function_call":
            return (
                "[tool_use " + str(p.get("name", "")) + "] "
                + str(p.get("arguments", ""))[:800]
            )
        elif ptype == "function_call_output":
            return "[tool_result] " + str(p.get("output", ""))[:800]
        elif ptype == "custom_tool_call":
            return (
                "[tool_use " + str(p.get("name", "")) + "] "
                + str(p.get("input", ""))[:800]
            )
        elif ptype == "custom_tool_call_output":
            return "[tool_result] " + str(p.get("output", ""))[:800]
        # reasoning items are encrypted/empty — skip
    elif typ == "event_msg":
        if ptype in ("agent_message", "user_message"):
            return "[" + str(ptype) + "] " + str(p.get("message", ""))
    return None


def _extract_snippet_gemini_chat(obj):
    """Gemini internal chat record (.gemini_state/tmp/<id>/chats/*.jsonl).

    Each message record: {"id", "timestamp", "type": user|gemini,
    "content": str|list, "toolCalls": [...]}. Rewind / metadata records
    ($rewindTo / $set) carry no behavior and are skipped.
    """
    if "$rewindTo" in obj or "$set" in obj:
        return None
    typ = obj.get("type")
    if typ not in ("user", "gemini"):
        return None
    parts = []
    content = obj.get("content")
    if isinstance(content, str):
        if content.strip():
            parts.append(content)
    elif isinstance(content, list):
        for c in content:
            if not isinstance(c, dict):
                continue
            if "text" in c:
                parts.append(str(c.get("text", "")))
            elif "functionCall" in c:
                fc = c.get("functionCall") or {}
                parts.append(
                    "[tool_use " + str(fc.get("name", "")) + "] "
                    + json.dumps(fc.get("args", {}), ensure_ascii=False)[:800]
                )
            elif "functionResponse" in c:
                parts.append(
                    "[tool_result] "
                    + json.dumps(c.get("functionResponse", {}), ensure_ascii=False)[:800]
                )
    tool_calls = obj.get("toolCalls")
    if isinstance(tool_calls, list):
        for tc in tool_calls:
            if isinstance(tc, dict):
                parts.append(
                    "[tool_use " + str(tc.get("name", "")) + "] "
                    + json.dumps(tc.get("args", {}), ensure_ascii=False)[:800]
                )
    if parts:
        return "[message role=" + str(typ) + "] " + "\n".join(p for p in parts if p)
    return None


def _detect_format(obj):
    """Identify which agent log format a JSONL record belongs to."""
    # Codex internal rollout: {type, payload}
    if obj.get("type") in ("session_meta", "response_item", "event_msg", "turn_context") \
            and isinstance(obj.get("payload"), dict):
        return "codex_rollout"
    # Gemini internal chat record: id + type user|gemini
    if "id" in obj and obj.get("type") in ("user", "gemini"):
        return "gemini_chat"
    # Claude stream or session jsonl: message dict
    if "message" in obj and isinstance(obj.get("message"), dict):
        return "claude"
    typ = obj.get("type", "")
    # Gemini stream-json
    if typ in ("init", "message", "tool_use", "tool_result", "result"):
        return "gemini"
    # Codex exec --json stream
    if typ in ("thread.started", "turn.started", "turn.completed",
               "item.completed", "item.started"):
        return "codex"
    return "unknown"


_EXTRACTORS = {
    "claude": _extract_snippet_claude,
    "gemini": _extract_snippet_gemini,
    "codex": _extract_snippet_codex,
    "codex_rollout": _extract_snippet_codex_rollout,
    "gemini_chat": _extract_snippet_gemini_chat,
}

# A snippet is "structural" when it records a concrete action — a tool call,
# tool result, shell command, or file change. Structural snippets are always
# retained (never keyword-filtered): they are the ground-truth evidence a
# validity judge must see, regardless of whether their text mentions a keyword.
_STRUCTURAL_RE = re.compile(r"\[(tool_use|tool_result|cmd\]|out |file_change)")


def _is_structural(snippet: str) -> bool:
    return bool(_STRUCTURAL_RE.search(snippet))


def excerpt_agent_log(log_path: Path, max_bytes: int = MAX_LOG_EXCERPT_BYTES) -> str:
    """Extract decision-relevant turns from an agent log.

    Supports both the streamed logs (claude/gemini/codex *.jsonl) and the
    agents' internal session files (claude session jsonl, codex rollout,
    gemini chat record).

    Retention policy:
      - Structural snippets (tool calls, tool results, shell commands,
        file changes) are ALWAYS kept — they are the actions the judge
        must see.
      - Non-structural snippets (free-text reasoning / messages) are kept
        only if they match the decision-relevant keyword pattern.

    When the kept snippets exceed ``max_bytes``, the TAIL is kept (earliest
    turns dropped): the final submission and any last-minute cheating happen
    at the end of the run.
    """
    if log_path is None or not log_path.exists():
        return ""
    pattern = re.compile(
        r"(\.fit\(|\.train\(|backward\(|optimizer|loss|model\b|"
        r"hardcode|hard-code|constant|random\.|np\.random|"
        r"/evaluate|/best_score|run\.py|workspace|"
        r"submission|prediction|output_dir|requests\.|"
        r"curl|api\.|openai|anthropic|gpt|claude|gemini|"
        r"\[cmd\]|\[out|\[tool_use|\[tool_result|\[file_change|\[result|\[message)",
        re.IGNORECASE,
    )
    # Pass 1: collect snippets. Lock onto the file's format from the first
    # recognizable record (the leading lines of a session file are often
    # bookkeeping records that detect as "unknown").
    snippets: List[str] = []
    fmt = None

    # Special-case: Gemini CLI's internal chat record is a single JSON
    # object (not JSONL): {"sessionId":..., "messages":[...]}. Detect from
    # the leading byte and parse the whole document, then iterate messages.
    try:
        with open(log_path, "rb") as _probe:
            _head = _probe.read(2048)
    except OSError as e:
        return "[failed to read log: " + str(e) + "]"
    _is_gemini_chat_doc = (
        _head.lstrip().startswith(b"{")
        and b'"messages"' in _head
        and (b'"sessionId"' in _head or b'"session_id"' in _head)
    )
    if _is_gemini_chat_doc:
        try:
            with open(log_path, "r", encoding="utf-8", errors="replace") as f:
                doc = json.load(f)
        except (OSError, json.JSONDecodeError) as e:
            return "[failed to parse gemini chat doc: " + str(e) + "]"
        if isinstance(doc, dict):
            for msg in doc.get("messages") or []:
                if not isinstance(msg, dict):
                    continue
                snippet = _extract_snippet_gemini_chat(msg)
                if not snippet:
                    continue
                snippet = snippet.strip()
                if not snippet:
                    continue
                structural = _is_structural(snippet)
                if not structural and not pattern.search(snippet):
                    continue
                if len(snippet) > 800:
                    snippet = snippet[:600] + " ... " + snippet[-150:]
                snippets.append(snippet)
        # Skip the line-by-line pass below; we have parsed the whole doc.
        _skip_line_pass = True
    else:
        _skip_line_pass = False

    try:
        if _skip_line_pass:
            f_iter = iter(())  # already parsed above
            cm_close = None
        else:
            cm_close = open(log_path, "rb")
            f_iter = cm_close
        try:
            for raw in f_iter:
                try:
                    line = raw.decode("utf-8", errors="replace").strip()
                except Exception:
                    continue
                if not line:
                    continue
                try:
                    obj = json.loads(line)
                except Exception:
                    continue
                if not isinstance(obj, dict):
                    continue
                if fmt is None:
                    detected = _detect_format(obj)
                    if detected != "unknown":
                        fmt = detected
                snippet = None
                extractor = _EXTRACTORS.get(fmt) if fmt else None
                if extractor is not None:
                    snippet = extractor(obj)
                if snippet is None:
                    # Format not locked yet, or this record type is not
                    # handled by the locked extractor — try all extractors.
                    for ex in _EXTRACTORS.values():
                        snippet = ex(obj)
                        if snippet:
                            break
                if not snippet:
                    continue
                snippet = snippet.strip()
                if not snippet:
                    continue
                structural = _is_structural(snippet)
                if not structural and not pattern.search(snippet):
                    continue
                if len(snippet) > 800:
                    snippet = snippet[:600] + " ... " + snippet[-150:]
                snippets.append(snippet)
        finally:
            if cm_close is not None:
                cm_close.close()
    except OSError as e:
        return "[failed to read log: " + str(e) + "]"
    # Pass 2: keep the tail within budget (walk backwards from the end).
    kept_rev: List[str] = []
    total_bytes = 0
    truncated = False
    for snippet in reversed(snippets):
        size = len(snippet.encode("utf-8")) + 1
        if total_bytes + size > max_bytes:
            truncated = True
            break
        total_bytes += size
        kept_rev.append(snippet)
    kept_lines = list(reversed(kept_rev))
    if truncated:
        kept_lines.insert(0, "... [earlier turns truncated; tail kept] ...")
    return "\n---\n".join(kept_lines)


def _find_state_log(task_out_dir: Path, agent_name: str) -> Optional[Path]:
    """Locate the agent's internal session file under .{agent}_state/.

    These files are the agent CLI's own conversation record — more complete
    than the streamed <agent>.jsonl. Layout differs per agent:
      - claude: .claude_state/projects/*/<session_id>.jsonl
      - codex:  .codex_state/sessions/YYYY/MM/DD/rollout-*-<session_id>.jsonl
      - gemini: .gemini_state/tmp/<project_id>/chats/*.jsonl

    Returns the session file matching the recorded session id when possible,
    otherwise the most recently modified candidate, or None.
    """
    def _newest(paths: List[Path]) -> Optional[Path]:
        existing = [p for p in paths if p.is_file()]
        if not existing:
            return None
        try:
            return max(existing, key=lambda p: p.stat().st_mtime)
        except OSError:
            return existing[0]

    if agent_name == "claude":
        proj_dir = task_out_dir / ".claude_state" / "projects"
        if not proj_dir.is_dir():
            return None
        cands = list(proj_dir.rglob("*.jsonl"))
        sid_file = task_out_dir / "claude_session_id.txt"
        if sid_file.exists():
            sid = sid_file.read_text(encoding="utf-8").strip()
            for c in cands:
                if c.stem == sid:
                    return c
        return _newest(cands)

    if agent_name == "codex":
        sess_dir = task_out_dir / ".codex_state" / "sessions"
        if not sess_dir.is_dir():
            return None
        cands = list(sess_dir.rglob("rollout-*.jsonl"))
        sid_file = task_out_dir / "codex_session_id.txt"
        if sid_file.exists():
            sid = sid_file.read_text(encoding="utf-8").strip()
            for c in cands:
                if sid in c.name:
                    return c
        return _newest(cands)

    if agent_name == "gemini":
        tmp_dir = task_out_dir / ".gemini_state" / "tmp"
        if not tmp_dir.is_dir():
            return None
        # Gemini CLI writes the chat record as <chats>/<session-id>.json
        # or session-<ts>-<sid8>.json. Older builds used .jsonl; accept both.
        cands: List[Path] = []
        for pat in ("*/chats/*.json", "*/chats/*.jsonl"):
            cands.extend(tmp_dir.glob(pat))
        sid_file = task_out_dir / "gemini_session_id.txt"
        if sid_file.exists():
            sid = sid_file.read_text(encoding="utf-8").strip()
            short = sid[:8]
            for c in cands:
                # Filename variants: <session_id>.{json,jsonl},
                #                    session-<ts>-<sid8>.{json,jsonl}.
                if c.stem == sid or sid in c.name or short in c.name:
                    return c
        return _newest(cands)

    return None


def collect_judge_inputs(task_out_dir: Path, agent_name: str = "claude") -> Dict[str, Any]:
    """Bundle the inputs needed for one judge call.

    Log source priority:
      1. The agent's internal session file under .{agent}_state/ (most complete).
      2. The streamed <agent>.jsonl produced by solve.py.
    If neither exists, the log excerpt is empty and judge_task reports a
    judge_error rather than guessing at an unrelated *.jsonl file.
    """
    workspace = task_out_dir / "workspace"
    log_path = _find_state_log(task_out_dir, agent_name)
    log_source = "state"
    if log_path is None or not log_path.exists():
        log_path = task_out_dir / f"{agent_name}.jsonl"
        log_source = "stream"
    return {
        "code_files": collect_code_files(workspace),
        "agent_log_excerpt": excerpt_agent_log(log_path),
        "log_source": log_source,
        "log_path": str(log_path) if log_path else None,
    }


def _build_user_prompt(inputs: Dict[str, Any]) -> str:
    """Format the inputs into the user message body."""
    parts: List[str] = []
    code_files = inputs.get("code_files", {})
    if code_files:
        parts.append("## Agent's Python source files (workspace/*.py)\n")
        for relpath, content in code_files.items():
            parts.append(f"### File: `{relpath}`\n")
            parts.append("```python\n" + content + "\n```\n")
    else:
        parts.append("## Agent's Python source files\n\n(no .py files found in workspace)\n")
    log_excerpt = inputs.get("agent_log_excerpt", "")
    parts.append("\n## Agent decision log excerpt (filtered)\n")
    if log_excerpt:
        parts.append("```\n" + log_excerpt + "\n```\n")
    else:
        parts.append("(no log excerpt available)\n")
    parts.append(
        "\nBased on the above, decide is_valid and provide reason. "
        "Output ONLY the JSON object as instructed."
    )
    return "\n".join(parts)


def _extract_json(text: str) -> Optional[Dict[str, Any]]:
    """Find the first {...} block in text and parse it."""
    if not text:
        return None
    # Try direct parse first
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    # Look for a JSON object substring (greedy match)
    start = text.find("{")
    if start < 0:
        return None
    depth = 0
    for i in range(start, len(text)):
        c = text[i]
        if c == "{":
            depth += 1
        elif c == "}":
            depth -= 1
            if depth == 0:
                snippet = text[start:i+1]
                try:
                    return json.loads(snippet)
                except json.JSONDecodeError:
                    return None
    return None


def call_anthropic_judge(
    user_prompt: str,
    model: str = DEFAULT_JUDGE_MODEL,
    base_url: str = DEFAULT_JUDGE_BASE_URL,
    api_key: Optional[str] = DEFAULT_JUDGE_API_KEY,
    timeout: int = DEFAULT_TIMEOUT,
) -> Tuple[bool, str]:
    """Single Anthropic Messages API call. Returns (success, raw_text_or_error)."""
    if not api_key:
        return (False, "no API key (set JUDGE_API_KEY or ANTHROPIC_API_KEY)")
    url = f"{base_url}/v1/messages"
    body = {
        "model": model,
        "max_tokens": 1024,
        "system": JUDGE_SYSTEM,
        "messages": [{"role": "user", "content": user_prompt}],
    }
    req = urllib.request.Request(
        url, data=json.dumps(body).encode("utf-8"),
        headers={
            "Content-Type": "application/json",
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
        },
    )
    try:
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            payload = json.loads(resp.read().decode("utf-8"))
    except urllib.error.HTTPError as e:
        try:
            err_body = e.read().decode("utf-8", errors="replace")
        except Exception:
            err_body = ""
        return (False, f"HTTP {e.code}: {err_body[:500]}")
    except (urllib.error.URLError, OSError) as e:
        return (False, f"network error: {e}")
    # Anthropic Messages API: content is a list of blocks; we want text blocks.
    content = payload.get("content", [])
    text_parts: List[str] = []
    for c in content:
        if isinstance(c, dict) and c.get("type") == "text":
            text_parts.append(str(c.get("text", "")))
    return (True, "".join(text_parts))


def apply_verdicts_to_results(
    results: List[Dict[str, Any]],
    verdicts: Dict[str, Dict[str, Any]],
) -> None:
    """Mutate results in place: add `judge` block + `effective_improvement`.

    effective_improvement rule:
      - is_valid is True   → effective_improvement = best_aggregate_improvement
      - is_valid is False  → effective_improvement = None (score discarded)
      - is_valid is None   → effective_improvement = best_aggregate_improvement
                             (judge errored; do not penalize agent)
    """
    for r in results:
        tn = r.get("task_name")
        verdict = verdicts.get(tn)
        best = r.get("best_aggregate_improvement")
        if verdict is None:
            r["judge"] = None
            r["effective_improvement"] = best
            continue
        r["judge"] = verdict
        if verdict.get("is_valid") is False:
            r["effective_improvement"] = None
        else:
            r["effective_improvement"] = best


